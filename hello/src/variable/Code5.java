package variable;

public class Code5 {
	public static void main(String[] args)
	{
		int a = 10, b = 27;
		
		System.out.println("a is " + a);
		System.out.println("b is " + b);
		System.out.println("a + b is " + a + b);
		System.out.println("(a + b) is " + (a + b));
		/* a is 10
		 * b is 27
		 * a + b is 1027
		 * (a + b) is 37 */
	}
}
